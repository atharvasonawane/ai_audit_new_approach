"""
task5/ui_consistency_checker.py
===============================
Reads Task 4 UI extractions and runs a two-pass consistency and spell check algorithm.
Writes defects directly to the UI_DEFECTS database table and outputs a summary JSON.

Two-pass Strategy:
1. First pass globally identifies dominant button casings and CSS classes.
2. Second pass spell-checks static/mixed text (after stripping interpolations) and 
   flags deviations in buttons from the globally dominant standards.

Outputs:
  - task5/ui_consistency_report.json
  - MySQL ui_defects table
"""

